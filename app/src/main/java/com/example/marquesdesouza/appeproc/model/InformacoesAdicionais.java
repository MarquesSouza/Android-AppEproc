public class InformacoesAdicionais {
    private Double valor;
    private String peticaoUrgente;
    private String vistaMinisterio;
    private String antecipacaoTutelar;
    private String prioridade;
    private String justicaGratuita;
    private String reuPreso;

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }

    public String getPeticaoUrgente() {
        return peticaoUrgente;
    }

    public void setPeticaoUrgente(String peticaoUrgente) {
        this.peticaoUrgente = peticaoUrgente;
    }

    public String getVistaMinisterio() {
        return vistaMinisterio;
    }

    public void setVistaMinisterio(String vistaMinisterio) {
        this.vistaMinisterio = vistaMinisterio;
    }

    public String getAntecipacaoTutelar() {
        return antecipacaoTutelar;
    }

    public void setAntecipacaoTutelar(String antecipacaoTutelar) {
        this.antecipacaoTutelar = antecipacaoTutelar;
    }

    public String getPrioridade() {
        return prioridade;
    }

    public void setPrioridade(String prioridade) {
        this.prioridade = prioridade;
    }

    public String getJusticaGratuita() {
        return justicaGratuita;
    }

    public void setJusticaGratuita(String justicaGratuita) {
        this.justicaGratuita = justicaGratuita;
    }

    public String getReuPreso() {
        return reuPreso;
    }

    public void setReuPreso(String reuPreso) {
        this.reuPreso = reuPreso;
    }
}
